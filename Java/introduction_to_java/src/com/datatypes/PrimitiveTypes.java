package com.datatypes;

public class PrimitiveTypes {

	public static void main(String[] args) {
		int age = 28;
		System.out.println("The value of int: " + age);
		
		byte bit = 124;
		System.out.println("The value of byte: " + bit);
		
		short value = 4508;
		System.out.println("The value of short: " + value);
		
		long phoneNum = 54956646L;
		System.out.println("The value of long int: " + phoneNum);
		
		float floatNum = 545.25f;
		System.out.println("The value of float: " + floatNum);
		
		double doubleNum = 54956.2656565;
		System.out.println("The value of double: " + doubleNum);
		
		char charVal = 'J';
		System.out.println("The value of char: " + charVal);
		
		boolean bol = false;
		System.out.println("The value of boolean: " + bol);
	}

}
